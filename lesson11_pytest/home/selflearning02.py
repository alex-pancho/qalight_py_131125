def advanced_calculator(*args, operation="sum", **kwargs):
    if operation == "sum":
        return sum(args)
    elif operation == "multiply":
        result = 1
        for num in args:
            result *= num
        return result
    elif operation == "max":
        return max(args)
    elif operation == "min":
        return min(args)

